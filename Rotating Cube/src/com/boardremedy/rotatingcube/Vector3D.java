package com.boardremedy.rotatingcube;

public class Vector3D 
{
	public double x;
	public double y;
	public double z;
	
	public Vector3D()
	{
		this.x = 0.0;
		this.y = 0.0;
		this.z = 0.0;
	}
	
	public Vector3D(double x, double y, double z)
	{
		this.x = x;
		this.y = y;
		this.z = z;
	}
}
